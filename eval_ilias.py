def eval_ilias(prompt):

    text_query = feature_extractor.extract_text_features([prompt])
    text_query = F.normalize(text_query)
    text_query = text_query.to(torch.float32)

    embeddings = clip_embeddings['embeddings'].to(torch.float32)
    image_keys = np.array(clip_embeddings['keys']).squeeze()

    """# Original CLIP"""

    original_clip_similarity = F.cosine_similarity(text_query, embeddings, dim=-1)
    ordered_clip = np.argsort(-original_clip_similarity.detach().numpy())
    ordered_scores_clip = original_clip_similarity[ordered_clip][:10]
    top10_matches_clip = image_keys[ordered_clip[:10]]

    """# QuARI"""

    # Forward pass
    out = hypernetwork.forward(text_query)

    # Get refined query and transformation matrix
    refined_query = out['refined_query']  # [B, E]

    W_image = out['W_image']              # [B, E, E]
    img_transformed = torch.bmm(embeddings.unsqueeze(0), W_image)  # [B, M, E]
    img_transformed = F.normalize(img_transformed, dim=-1)

    # Compute similarity
    scores_quari = torch.einsum('be,bme->bm', refined_query, img_transformed).detach().numpy().squeeze()

    ordered_quari = np.argsort(-scores_quari)
    ordered_scores_quari = scores_quari[ordered_quari][:10]
    top10_matches_quari = image_keys[ordered_quari[:10]]

    """# Visualize"""

    # --- Visualization ---
    fig, axes = plt.subplots(2, 10, figsize=(25, 8))
    #title with prompt
    fig.suptitle(f"Prompt: {prompt}", fontsize=16)

    # Top row: CLIP
    for i, (match, score) in enumerate(zip(top10_matches_clip, ordered_scores_clip)):
        ax = axes[0, i]
        ax.imshow(web_dataset_lookup[match])
        ax.set_title(f"{score.item():.3f}", fontsize=10)
        ax.axis("off")

    # Bottom row: QuARI
    for i, (match, score) in enumerate(zip(top10_matches_quari, ordered_scores_quari)):
        ax = axes[1, i]
        ax.imshow(web_dataset_lookup[match])
        ax.set_title(f"{score.item():.3f}", fontsize=10)
        ax.axis("off")

    # --- Row labels on the left ---
    for row_label, ax in zip(["CLIP", "QuARI"], axes[:, 0]):
        ax.text(
            0, 0.5, row_label,
            va="center", ha="right",
            fontsize=14, fontweight="bold",
            rotation=90, transform=ax.transAxes
        )

    plt.tight_layout()
    plt.show()

    # save figure under query name
    clean_prompt = prompt.replace(" ", "_")[:20]
    plt.savefig(f"{output_dir}/{clean_prompt}_{stem}.png")

for prompt in prompts:
    eval_ilias(prompt)