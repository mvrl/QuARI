def build_ilias_flat_dataset(shard_dir: str | Path):
    shard_dir = Path(shard_dir)
    shards = sorted(str(shard_dir / f) for f in os.listdir(shard_dir) if f.endswith(".tar"))

    ds = (
        wds.WebDataset(shards, repeat=False)
        # keep only samples that actually have an image under "jpg"
        .select(lambda s: ("__key__" in s) and ("jpg" in s))
        .decode("pil")
        .to_tuple("__key__", "jpg")                 # -> (key, PIL.Image)
        .map_tuple(lambda k: k, lambda im: im.convert("RGB"))
    )
    return ds

